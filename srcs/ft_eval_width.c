#include "../includes/ft_printf.h"
#include "../libft/libft.h"

int ft_len(int num)
{
	int i;

	i = 0;
	if (num == 0)
		return (i);
	while (num > 0)
	{
		i++;
		num /= 10;
	}
	return (i - 1);
}

int ft_eval_width(t_print *mytab, const char *format, int pos)
{
	int i;

	i = 0;
	while (ft_isdigit(format[pos]))
		i = i * 10 + ft_atoi(&format[pos++]);
	mytab->width = i;
	pos += ft_len(i);
	return (pos);
}
